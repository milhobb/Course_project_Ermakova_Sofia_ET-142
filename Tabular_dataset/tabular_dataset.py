    if pop < 1000:
        return 'Очень малое (<1 млн)'
    elif pop < 10000:
        return 'Малое (1-10 млн)'
    elif pop < 100000:
        return 'Среднее (10-100 млн)'
    else:
        return 'Крупное (>100 млн)'

